def create_line(date):
    global nums
    answer = [[] for i in range(8)]
    for number in range(0, len(date)):
        for r in range(0, 8):
            answer[r] += nums[date[number]][r]
    return answer







while True:
    hour = os.popen('date +"%H %M %S"').read()
    hour = hour.replace(' ', ':').replace('\n', '')
    os.system('clear')
    #x, y = os.get_terminal_size()
    x = create_line(hour)

    for i in x:
        print(*i)
